#code to set a variable x equal to a value and print the value

x = 2 #sets variable x equal to 2, this is an assignment statement

x = x + 2 #sets the variable x equal to the previous value of x + 2, the is an assignment with expression

print ("The value of the variable x is: ", (x)) #prints the value of x, this is a print statement

