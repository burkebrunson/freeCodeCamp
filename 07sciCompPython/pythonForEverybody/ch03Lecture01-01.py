#code of Boolean expression conditional statements using comparison operators

x = int (input ("Please enter a value: ")) #user input prompt for a value

if x == 5:  #boolean expression with True/False answer
    print ("Your variable equals 5")  #code to run conditionally depending on outcome of boolean expression
if x > 5:
    print ("Your variable is greater than 4")
if x >= 5:
    print ("Your variable is greater than or equal to 5")
if x < 6:
    print ("Your variable is less than 6")
if x <= 5:
    print ("Your variable is less than or equal to 5")
if x != 6:
    print ("Your variable does not equal 6")
